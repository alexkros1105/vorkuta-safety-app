export function generateShiftSchedule() {
  const shifts = [];
  for (let d = 0; d < 30; d++) {
    const date = new Date();
    date.setDate(date.getDate() - 10 + d);
    const dateStr = date.toISOString().split('T')[0];
    const dayMod = d % 4;
    let shift_type: string;
    if (dayMod === 0) shift_type = 'day';
    else if (dayMod === 1) shift_type = 'night';
    else shift_type = 'day_off';
    shifts.push({
      id: d + 1,
      employee_id: 1,
      shift_date: dateStr,
      shift_type,
      section: 'Участок №3 (Шахта Воркутинская)',
    });
  }
  return shifts;
}
